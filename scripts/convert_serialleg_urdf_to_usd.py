def _validate_tree_joints(inventory: StageInventory) -> list[tuple[str, str]]:
    from pxr import UsdPhysics

    tree_edges = []
    for name, (expected_parent, expected_child, expected_armature) in EXPECTED_TREE_JOINTS.items():
        prim = inventory.revolute_joints[name]
        joint = UsdPhysics.Joint(prim)
        parent, parent_path = _relationship_target_name(joint, "physics:body0")
        child, child_path = _relationship_target_name(joint, "physics:body1")
        if (parent, child) != (expected_parent, expected_child):
            raise ConversionError(
                f"{name} body relationship changed: expected {expected_parent}->{expected_child}, got {parent}->{child}"
            )
        if parent_path != inventory.links[parent].GetPath() or child_path != inventory.links[child].GetPath():
            raise ConversionError(f"{name} body relationship does not target the validated link prims")
        excluded = joint.GetExcludeFromArticulationAttr()
        if excluded and excluded.HasAuthoredValueOpinion() and bool(excluded.Get()):
            raise ConversionError(f"tree joint {name} must remain inside the articulation")
        armature = prim.GetAttribute("physxJoint:armature")
        axis_armature = prim.GetAttribute("physxJointAxis:angular:armature")
        if not armature or not armature.HasAuthoredValueOpinion():
            raise ConversionError(f"{name} is missing authored joint armature")
        if not axis_armature or not axis_armature.HasAuthoredValueOpinion():
            raise ConversionError(f"{name} is missing authored angular-axis armature")
        _assert_close(float(armature.Get()), expected_armature, f"{name} joint armature")
        _assert_close(float(axis_armature.Get()), expected_armature, f"{name} angular armature")
        viscous_friction = prim.GetAttribute("physxJointAxis:angular:viscousFrictionCoefficient")
        if not viscous_friction or not viscous_friction.HasAuthoredValueOpinion():
            raise ConversionError(f"{name} is missing authored viscous damping")
        _assert_close(
            float(viscous_friction.Get()),
            math.radians(EXPECTED_JOINT_DAMPING[name]),
            f"{name} viscous damping",
        )
        drive = UsdPhysics.DriveAPI.Get(prim, "angular")
        if not drive:
            raise ConversionError(f"{name} is missing the neutral angular DriveAPI")
        if drive.GetTypeAttr().Get() != "force":
            raise ConversionError(f"{name} drive type must be force")
        for attribute, label in (
            (drive.GetStiffnessAttr(), "stiffness"),
            (drive.GetDampingAttr(), "damping"),
            (drive.GetMaxForceAttr(), "max force"),
        ):
            if not attribute.HasAuthoredValueOpinion():
                raise ConversionError(f"{name} drive {label} must be explicitly authored")
            _assert_close(float(attribute.Get()), 0.0, f"{name} drive {label}")
        for attribute, label in (
            (drive.GetTargetPositionAttr(), "target position"),
            (drive.GetTargetVelocityAttr(), "target velocity"),
        ):
            if attribute.Get() is not None:
                raise ConversionError(f"{name} drive {label} must be blocked")
        for attribute_name in ("physics:lowerLimit", "physics:upperLimit"):
            limit = prim.GetAttribute(attribute_name)
            if limit and limit.HasAuthoredValueOpinion() and math.isfinite(float(limit.Get())):
                raise ConversionError(f"{name} must not have a finite independent joint limit")
        tree_edges.append((parent, child))
    return tree_edges


def _validate_connected_tree(tree_edges: list[tuple[str, str]]) -> None:
    adjacency = {link: set() for link in EXPECTED_LINKS}
    for parent, child in tree_edges:
        adjacency[parent].add(child)
        adjacency[child].add(parent)
    visited = set()
    pending = ["base_link"]
    while pending:
        link = pending.pop()
        if link in visited:
            continue
        visited.add(link)
        pending.extend(adjacency[link] - visited)
    if visited != EXPECTED_LINKS or len(tree_edges) != len(EXPECTED_LINKS) - 1:
        raise ConversionError("the articulation joint graph is not one connected tree")


def _validate_loop_joints(inventory: StageInventory) -> None:
    from pxr import UsdPhysics

    for name, (expected_body0, position0, expected_body1, position1) in EXPECTED_LOOPS.items():
        prim = inventory.spherical_joints[name]
        joint = UsdPhysics.Joint(prim)
        body0, body0_path = _relationship_target_name(joint, "physics:body0")
        body1, body1_path = _relationship_target_name(joint, "physics:body1")
        if (body0, body1) != (expected_body0, expected_body1):
            raise ConversionError(
                f"{name} body relationship changed: expected {expected_body0}->{expected_body1}, got {body0}->{body1}"
            )
        if body0_path != inventory.links[body0].GetPath() or body1_path != inventory.links[body1].GetPath():
            raise ConversionError(f"{name} does not target the validated rigid-body link prims")
        _assert_vector_close(joint.GetLocalPos0Attr().Get(), position0, f"{name}.localPos0")
        _assert_vector_close(joint.GetLocalPos1Attr().Get(), position1, f"{name}.localPos1")
        for suffix, quaternion in (
            ("localRot0", joint.GetLocalRot0Attr().Get()),
            ("localRot1", joint.GetLocalRot1Attr().Get()),
        ):
            _assert_close(abs(float(quaternion.GetReal())), 1.0, f"{name}.{suffix}.real")
            _assert_vector_close(quaternion.GetImaginary(), (0.0, 0.0, 0.0), f"{name}.{suffix}.imaginary")
        excluded = joint.GetExcludeFromArticulationAttr()
        if not excluded or not excluded.HasAuthoredValueOpinion() or excluded.Get() is not True:
            raise ConversionError(f"{name} must author physics:excludeFromArticulation=true")
        if joint.GetJointEnabledAttr().Get() is not True:
            raise ConversionError(f"{name} must be enabled")
        armature = prim.GetAttribute("physxJoint:armature")
        if not armature or not armature.HasAuthoredValueOpinion():
            raise ConversionError(f"{name} is missing loop-joint armature")
        _assert_close(float(armature.Get()), LOOP_JOINT_ARMATURE, f"{name} armature")
        if any("PhysicsDriveAPI" in schema for schema in prim.GetAppliedSchemas()):
            raise ConversionError(f"{name} must not have a drive API")


def _validate_mass_and_shapes(inventory: StageInventory) -> tuple[float, int]:
    from pxr import UsdGeom, UsdPhysics

    total_mass = 0.0
    for name, prim in inventory.links.items():
        mass = UsdPhysics.MassAPI(prim).GetMassAttr()
        if not mass or not mass.HasAuthoredValueOpinion():
            raise ConversionError(f"{name} has no authored mass")
        total_mass += float(mass.Get())
    _assert_close(total_mass, EXPECTED_TOTAL_MASS, "USD total mass", tolerance=1.0e-6)

    collision_root_links = {prim.GetParent().GetName() for prim in inventory.collision_roots}
    if collision_root_links != EXPECTED_LINKS or len(inventory.collision_roots) != len(EXPECTED_LINKS):
        paths = [str(prim.GetPath()) for prim in inventory.collision_roots]
        raise ConversionError(f"expected one collision scope per link, got {paths}")
    if inventory.visual_roots:
        paths = [str(prim.GetPath()) for prim in inventory.visual_roots]
        raise ConversionError(f"collision-only USD contains active visual scopes: {paths}")
    if inventory.visual_geometry:
        paths = [str(prim.GetPath()) for prim in inventory.visual_geometry]
        raise ConversionError(f"collision-only USD contains active visual geometry: {paths}")
    if len(inventory.geometry_prims) != EXPECTED_COLLISION_MESH_COUNT + 2:
        raise ConversionError(f"expected 56 collision geometry prims, got {len(inventory.geometry_prims)}")
    if len(inventory.collision_geometry) != len(inventory.geometry_prims):
        paths = [str(prim.GetPath()) for prim in inventory.geometry_prims if prim not in inventory.collision_geometry]
        raise ConversionError(f"active geometry exists outside collision scopes: {paths}")
    if inventory.non_collision_geometry:
        paths = [str(prim.GetPath()) for prim in inventory.non_collision_geometry]
        raise ConversionError(f"collision-only USD contains visual or unclassified geometry: {paths}")
    if inventory.authored_non_collision_geometry:
        paths = [str(prim.GetPath()) for prim in inventory.authored_non_collision_geometry]
        raise ConversionError(f"flattened USD contains orphan non-collision geometry: {paths}")
    if len(inventory.collision_api_prims) != EXPECTED_COLLISION_MESH_COUNT + 2:
        raise ConversionError(f"expected 56 CollisionAPI prims, got {len(inventory.collision_api_prims)}")
    if any("collisions" not in str(prim.GetPath()).split("/") for prim in inventory.collision_api_prims):
        paths = [str(prim.GetPath()) for prim in inventory.collision_api_prims]
        raise ConversionError(f"CollisionAPI prim exists outside collision scopes: {paths}")
    non_geometry_colliders = [prim for prim in inventory.collision_api_prims if not prim.IsA(UsdGeom.Gprim)]
    if non_geometry_colliders:
        paths = [str(prim.GetPath()) for prim in non_geometry_colliders]
        raise ConversionError(f"collision schemas must be authored on geometry leaves, not wrappers: {paths}")
    disabled_colliders = [
        prim
        for prim in inventory.collision_api_prims
        if UsdPhysics.CollisionAPI(prim).GetCollisionEnabledAttr().Get() is not True
    ]
    if disabled_colliders:
        paths = [str(prim.GetPath()) for prim in disabled_colliders]
        raise ConversionError(f"collision geometry is unexpectedly disabled: {paths}")
    if len(inventory.mesh_collision_api_prims) != EXPECTED_COLLISION_MESH_COUNT:
        raise ConversionError(
            f"expected {EXPECTED_COLLISION_MESH_COUNT} mesh collision API prims, "
            f"got {len(inventory.mesh_collision_api_prims)}"
        )
    if len(inventory.meshes) != EXPECTED_COLLISION_MESH_COUNT:
        raise ConversionError(f"expected {EXPECTED_COLLISION_MESH_COUNT} collision meshes, got {len(inventory.meshes)}")
    mesh_without_direct_collider = [prim for prim in inventory.meshes if not prim.HasAPI(UsdPhysics.CollisionAPI)]
    if mesh_without_direct_collider:
        paths = [str(prim.GetPath()) for prim in mesh_without_direct_collider]
        raise ConversionError(f"collision mesh lacks direct CollisionAPI: {paths}")
    mesh_without_direct_mesh_collider = [
        prim for prim in inventory.meshes if not prim.HasAPI(UsdPhysics.MeshCollisionAPI)
    ]
    if mesh_without_direct_mesh_collider:
        paths = [str(prim.GetPath()) for prim in mesh_without_direct_mesh_collider]
        raise ConversionError(f"collision mesh lacks direct MeshCollisionAPI: {paths}")
    unexpected_mesh_collision_api_prims = [
        prim for prim in inventory.mesh_collision_api_prims if not prim.IsA(UsdGeom.Mesh)
    ]
    if unexpected_mesh_collision_api_prims:
        paths = [str(prim.GetPath()) for prim in unexpected_mesh_collision_api_prims]
        raise ConversionError(f"MeshCollisionAPI must be authored on Mesh prims: {paths}")
    non_convex_hull_meshes = [
        prim
        for prim in inventory.meshes
        if UsdPhysics.MeshCollisionAPI(prim).GetApproximationAttr().Get() != UsdPhysics.Tokens.convexHull
    ]
    if non_convex_hull_meshes:
        paths = [str(prim.GetPath()) for prim in non_convex_hull_meshes]
        raise ConversionError(f"collision mesh approximation changed from convexHull: {paths}")
    if len(inventory.cylinders) != 2:
        raise ConversionError(f"expected two preserved wheel cylinders, got {len(inventory.cylinders)}")
    if inventory.capsules:
        raise ConversionError(
            f"cylinder replacement was unexpectedly enabled: found {len(inventory.capsules)} capsules"
        )
    for cylinder in inventory.cylinders:
        cylinder_schema = cylinder if hasattr(cylinder, "GetRadiusAttr") else None
        if cylinder_schema is None:
            cylinder_schema = UsdGeom.Cylinder(cylinder)
        _assert_close(float(cylinder_schema.GetRadiusAttr().Get()), 0.06, f"{cylinder.GetPath()} radius")
        _assert_close(float(cylinder_schema.GetHeightAttr().Get()), 0.02, f"{cylinder.GetPath()} height")

    face_count = 0
    point_count = 0
    index_count = 0
    for prim in inventory.meshes:
        mesh = UsdGeom.Mesh(prim)
        face_count += len(mesh.GetFaceVertexCountsAttr().Get() or [])
        point_count += len(mesh.GetPointsAttr().Get() or [])
        index_count += len(mesh.GetFaceVertexIndicesAttr().Get() or [])
    if face_count != EXPECTED_COLLISION_FACE_COUNT:
        raise ConversionError(f"collision mesh face count changed: expected 7102, got {face_count}")
    if point_count != EXPECTED_COLLISION_POINT_INDEX_COUNT:
        raise ConversionError(f"collision mesh point count changed: expected 21306, got {point_count}")
    if index_count != EXPECTED_COLLISION_POINT_INDEX_COUNT:
        raise ConversionError(f"collision mesh index count changed: expected 21306, got {index_count}")
    return total_mass, face_count


def _validate_saved_usd(usd_path: Path, source: SourceContract) -> dict[str, Any]:
    from pxr import Usd, UsdGeom, UsdPhysics, UsdUtils

    if not usd_path.is_file():
        raise ConversionError(f"USD does not exist: {usd_path}")
    usd_size = usd_path.stat().st_size
    if usd_size > MAX_COLLISION_ONLY_USD_BYTES:
        raise ConversionError(
            f"collision-only USD is unexpectedly large: {usd_size} bytes > {MAX_COLLISION_ONLY_USD_BYTES}"
        )
    stage = Usd.Stage.Open(str(usd_path), Usd.Stage.LoadAll)
    if stage is None:
        raise ConversionError(f"failed to open USD: {usd_path}")
    _assert_close(float(UsdGeom.GetStageMetersPerUnit(stage)), 1.0, "USD metersPerUnit")
    if UsdGeom.GetStageUpAxis(stage) != UsdGeom.Tokens.z:
        raise ConversionError(f"USD up axis must be Z, got {UsdGeom.GetStageUpAxis(stage)}")
    if any(prim.IsA(UsdPhysics.Scene) for prim in stage.Traverse()):
        raise ConversionError("generated robot asset must not contain a PhysicsScene")

    default_prim = _validate_layer_metadata(stage, source)
    inventory = _collect_stage_inventory(stage)
    _validate_physics_prim_paths(inventory, default_prim)
    articulation_root = _validate_articulation(inventory)
    _validate_joint_sets(inventory)
    _validate_connected_tree(_validate_tree_joints(inventory))
    _validate_loop_joints(inventory)
    total_mass, collision_faces = _validate_mass_and_shapes(inventory)

    _layers, _assets, unresolved = UsdUtils.ComputeAllDependencies(str(usd_path))
    if unresolved:
        raise ConversionError(f"USD has unresolved dependencies: {sorted(str(path) for path in unresolved)}")

    return {
        "default_prim": str(default_prim.GetPath()),
        "articulation_root": str(articulation_root.GetPath()),
        "links": len(inventory.links),
        "tree_joints": len(inventory.revolute_joints),
        "loop_joints": len(inventory.spherical_joints),
        "total_mass": total_mass,
        "usd_bytes": usd_size,
        "collision_meshes": len(inventory.meshes),
        "collision_faces": collision_faces,
        "visual_geometries": len(inventory.non_collision_geometry),
        "cylinders": len(inventory.cylinders),
    }


def _import_to_usd(urdf_path: Path, output_path: Path, source: SourceContract) -> dict[str, Any]:
    print("[serialleg-usd] loading URDF importer modules...", flush=True)
    import omni.kit.commands
    from isaacsim.asset.importer.urdf import _urdf
    from pxr import Usd

    print("[serialleg-usd] URDF importer modules are ready", flush=True)

    output_path.parent.mkdir(parents=True, exist_ok=True)
    with tempfile.TemporaryDirectory(prefix="serialleg_urdf_import_") as temporary_directory:
        temporary_root = Path(temporary_directory)
        import_urdf_path = _build_collision_only_import_urdf(urdf_path, temporary_root)
        imported_path = temporary_root / output_path.name

        config = _urdf.ImportConfig()
        setters = (
            ("fix_base", config.set_fix_base, IMPORTER_SETTINGS["fix_base"]),
            ("merge_fixed_joints", config.set_merge_fixed_joints, IMPORTER_SETTINGS["merge_fixed_joints"]),
            ("import_inertia_tensor", config.set_import_inertia_tensor, IMPORTER_SETTINGS["import_inertia_tensor"]),
            ("make_default_prim", config.set_make_default_prim, IMPORTER_SETTINGS["make_default_prim"]),
            ("create_physics_scene", config.set_create_physics_scene, IMPORTER_SETTINGS["create_physics_scene"]),
            ("self_collision", config.set_self_collision, IMPORTER_SETTINGS["self_collision"]),
            (
                "collision_from_visuals",
                config.set_collision_from_visuals,
                IMPORTER_SETTINGS["collision_from_visuals"],
            ),
            (
                "replace_cylinders_with_capsules",
                config.set_replace_cylinders_with_capsules,
                IMPORTER_SETTINGS["replace_cylinders_with_capsules"],
            ),
        )
        for _setting_name, setter, value in setters:
            setter(value)

        print(
            "[serialleg-usd] importer configuration is ready; "
            f"stripped_visuals={EXPECTED_VISUAL_COUNT} transient_dummy_visuals={len(EXPECTED_LINKS)} "
            f"retained_collision_meshes={EXPECTED_COLLISION_MESH_COUNT}",
            flush=True,
        )
        print(f"[serialleg-usd] importing URDF into temporary stage: {imported_path}", flush=True)
        status, prim_path = omni.kit.commands.execute(
            "URDFParseAndImportFile",
            urdf_path=str(import_urdf_path),
            import_config=config,
            dest_path=str(imported_path),
        )
        if not status:
            raise ConversionError(f"Isaac URDF importer failed for {urdf_path}")
        print(f"[serialleg-usd] importer returned root prim: {prim_path}", flush=True)

        stage = Usd.Stage.Open(str(imported_path), Usd.Stage.LoadAll)
        if stage is None:
            raise ConversionError(f"failed to open importer output: {imported_path}")
        if not stage.GetPrimAtPath(prim_path):
            raise ConversionError(f"importer returned invalid prim path: {prim_path}")
        print("[serialleg-usd] removing transient dummy visual scopes...", flush=True)
        _remove_transient_visuals(stage, prim_path)
        print("[serialleg-usd] applying external-loop and joint-physics postprocess...", flush=True)
        _postprocess_imported_stage(stage, source)

        with tempfile.NamedTemporaryFile(
            prefix=f".{output_path.stem}.",
            suffix=".usd",
            dir=output_path.parent,
            delete=False,
        ) as stream:
            atomic_path = Path(stream.name)
        try:
            if not stage.Export(str(atomic_path)):
                raise ConversionError(f"failed to flatten imported stage into {atomic_path}")
            print("[serialleg-usd] normalizing mesh collision schemas onto Mesh leaves...", flush=True)
            _normalize_saved_mesh_collision_schemas(atomic_path)
            _canonicalize_flattened_stage(atomic_path)
            print("[serialleg-usd] validating flattened USD from disk...", flush=True)
            summary = _validate_saved_usd(atomic_path, source)
            os.replace(atomic_path, output_path)
            output_path.chmod(0o644)
        finally:
            atomic_path.unlink(missing_ok=True)

    return summary


def main() -> int:
    args = _parse_args()
    app = None
    try:
        urdf_path = args.urdf.resolve()
        output_path = args.output.resolve()
        source = _validate_urdf(urdf_path)

        print("[serialleg-usd] starting Isaac Sim 5.1 headless...", flush=True)
        app = _launch_simulation_app()
        print("[serialleg-usd] Isaac Sim is ready", flush=True)
        if args.check:
            summary = _validate_saved_usd(output_path, source)
            action = "checked"
        else:
            summary = _import_to_usd(urdf_path, output_path, source)
            action = "wrote"

        print(f"[serialleg-usd] {action}: {output_path}", flush=True)
        print(
            "[serialleg-usd] "
            f"default_prim={summary['default_prim']} articulation_root={summary['articulation_root']} "
            f"links={summary['links']} tree_joints={summary['tree_joints']} "
            f"external_loop_joints={summary['loop_joints']} collision_meshes={summary['collision_meshes']} "
            f"collision_faces={summary['collision_faces']} cylinders={summary['cylinders']} "
            f"visual_geometries={summary['visual_geometries']} usd_bytes={summary['usd_bytes']} "
            f"total_mass={summary['total_mass']:.8f}kg",
            flush=True,
        )
        app.close(wait_for_replicator=False)
        return 0
    except (ConversionError, ET.ParseError, ImportError, OSError, RuntimeError, ValueError) as error:
        print(f"[serialleg-usd][error] {type(error).__name__}: {error}", file=sys.stderr, flush=True)
        # Kit's fast shutdown exits the process with status 0 before Python can
        # return from close().  On a validated error path, terminate directly
        # so CI and --check callers receive the correct failure status.
        if app is not None:
            sys.stdout.flush()
            sys.stderr.flush()
            os._exit(1)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())

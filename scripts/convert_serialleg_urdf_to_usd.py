#!/usr/bin/env python3
"""Import the canonical SerialLeg loop-joint URDF into a validated USD asset.

The Isaac Sim URDF importer creates a reduced-coordinate articulation from the
ten tree joints and emits the two custom ``loop_joint`` elements as external
``PhysicsSphericalJoint`` constraints.  This script makes the critical
``excludeFromArticulation`` opinions explicit, restores the MJCF joint
armatures, flattens the imported stage, and validates the saved artifact.  The
canonical URDF and runtime USD are both collision-only; import adds only tiny
transient dummy visuals required by the importer and removes them before save.
"""


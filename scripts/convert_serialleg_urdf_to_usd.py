def _load_serialleg_contract() -> Any:
    """Load the pure-Python contract module without importing the IsaacLab extension package."""
    module_path = ASSET_DIR.parent / "serialleg_contract.py"
    module_name = "_se3_rl_lab_serialleg_contract"
    spec = importlib.util.spec_from_file_location(module_name, module_path)
    if spec is None or spec.loader is None:
        raise RuntimeError(f"failed to load SerialLeg contract module: {module_path}")
    module = importlib.util.module_from_spec(spec)
    sys.modules[module_name] = module
    spec.loader.exec_module(module)
    return module.load_serialleg_contract(ASSET_DIR / "serialleg_contract.toml")


